package com.ex.route_service.enums;

/**
 * Типы событий маршрута курьера, фиксируемые во время сессии перемещения.
 */
public enum RouteEventStatus {
    /**
     * Курьер начал маршрут (начало перемещения).
     */
    START,

    /**
     * Курьер приостановил движение (например, остановка на обед).
     */
    PAUSE,

    /**
     * Курьер возобновил маршрут после паузы.
     */
    RESUME,

    /**
     * Курьер завершил маршрут (конец смены или доставка завершена).
     */
    END,

    /**
     * Курьер отклонился от предполагаемого маршрута.
     */
    DEVIATION,

    /**
     * Курьер прибыл в точку назначения.
     */
    ARRIVAL,

    /**
     * Курьер покинул точку (например, уехал от клиента).
     */
    DEPARTURE,

    /**
     * Курьер забрал заказ (например, из магазина или пункта).
     */
    PICKUP,

    /**
     * Курьер доставил заказ клиенту.
     */
    DROPOFF,

    /**
     * Курьер ожидает (например, клиента или загрузку товара).
     */
    WAIT,

    /**
     * Маршрут был отменён (возможно, заказ отменён или возникла ошибка).
     */
    CANCEL,
}
