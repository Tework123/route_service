package com.ex.route_service.enums;

/**
 * Типы событий маршрута курьера, фиксируемые во время рабочей смены.
 */
public enum RouteEventStatus {
    /**
     * Курьер начал смену.
     */
    SHIFT_STARTED,

    /**
     * Курьер взял заказ в работу.
     */
    TAKE_ORDER,

    /**
     * Курьер прибыл в точку получения заказа (на склад, магазин и т.п.).
     */
    ARRIVED_AT_PICKUP_POINT,

    /**
     * Курьер успешно забрал заказ у отправителя.
     */
    ORDER_PICKED_UP,

    /**
     * Курьер прибыл по адресу доставки.
     */
    ARRIVED_AT_DELIVERY_POINT,

    /**
     * Курьер успешно доставил заказ получателю.
     */
    ORDER_DELIVERED,

    /**
     * Курьер отменил заказ (по каким-либо причинам).
     */
    ORDER_CANCELLED,

    /**
     * Курьер вышел на паузу (например, обед).
     */
    PAUSE_STARTED,

    /**
     * Курьер вернулся с паузы и продолжил работу.
     */
    PAUSE_FINISHED,

    /**
     * Курьер сменил транспортное средство во время смены.
     */
    VEHICLE_CHANGED,


    /**
     * Курьер завершил смену.
     */
    SHIFT_FINISHED,

    /**
     * Курьер сообщил о проблеме или зафиксировано отклонение от маршрута.
     */
    ISSUE_REPORTED
}
